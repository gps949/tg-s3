export type Lang = 'en' | 'zh' | 'ja' | 'fr';
export const SUPPORTED_LANGS: Lang[] = ['en', 'zh', 'ja', 'fr'];

export function detectLang(request: Request): Lang {
  const url = new URL(request.url);
  const q = url.searchParams.get('lang');
  if (q && SUPPORTED_LANGS.includes(q as Lang)) return q as Lang;
  const accept = request.headers.get('accept-language') || '';
  for (const part of accept.split(',')) {
    const code = part.trim().split(';')[0].split('-')[0].toLowerCase();
    if (SUPPORTED_LANGS.includes(code as Lang)) return code as Lang;
  }
  return 'en';
}

