export function detectBotLang(languageCode?: string): Lang {
  if (!languageCode) return 'en';
  const code = languageCode.split('-')[0].toLowerCase();
  if (SUPPORTED_LANGS.includes(code as Lang)) return code as Lang;
  return 'en';
}

export function botT(lang: Lang, key: string, ...args: (string | number)[]): string {
  const str = botStrings[lang]?.[key] || botStrings.en[key] || key;
  return str.replace(/\{(\d+)\}/g, (_, i) => String(args[+i] ?? ''));
}

